// 'Transmission Control Protocol (TCP) ensures that bits arrive at the correct
// destination in the correct order.


// How does TCP work?
// One computer waits and 'listens' for another computer to talk to it. 
// To be able to listen for different kinds of communication at the same time on a single
// computer, each listener has a number(called a port) associated with it. 

// Ports are usually specified for specific communications. 

// The listening computer is called the 'server'.
// The connecting computer is called the 'client'.

